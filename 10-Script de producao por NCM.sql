--SINTETICO

SELECT D3_FILIAL, B1_POSIPI, SUM (D3_QUANT) FROM SD3010 

INNER JOIN SB1010 ON B1_COD=D3_COD AND SB1010.D_E_L_E_T_='' AND B1_POSIPI LIKE '3806%'

WHERE D3_EMISSAO BETWEEN '20211101' AND '20211130' AND D3_CF='PR0'  AND SD3010.D_E_L_E_T_=''

GROUP BY D3_FILIAL, B1_POSIPI



