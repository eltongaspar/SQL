WITH ProductList AS (
    SELECT '0240420401' AS ProductCode
    UNION ALL SELECT '0240421401'
    UNION ALL SELECT '0240422401'
    UNION ALL SELECT '0240423401'
    UNION ALL SELECT '0240502401'
    UNION ALL SELECT '0240520401'
    UNION ALL SELECT '0240521401'
    UNION ALL SELECT '0240522401'
    UNION ALL SELECT '0240523401'
    UNION ALL SELECT '0240602401'
    UNION ALL SELECT '0240620401'
    UNION ALL SELECT '0240621401'
    UNION ALL SELECT '0240622401'
    UNION ALL SELECT '0240623401'
)
SELECT 
    PL.ProductCode,
    COALESCE(
        CASE 
            WHEN SB.B1_COD IS NOT NULL THEN 'Produto Cadastrado'
            ELSE 'Produto Não Cadastrado'
        END,
        'Produto Não Cadastrado'
    ) AS Produto_Status
FROM 
    ProductList PL
LEFT JOIN 
    SB1010 SB ON PL.ProductCode = SB.B1_COD AND SB.D_E_L_E_T_ = ''
ORDER BY 
    PL.ProductCode;
