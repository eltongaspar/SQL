SELECT 
E3_CODCLI+' - '+A1_NOME, 
E3_NUM, 
SUBSTRING(E3_EMISSAO,7,2)+'/'+SUBSTRING(E3_EMISSAO,5,2)+'/'+SUBSTRING(E3_EMISSAO,1,4) AS EMISSAO, 
SUBSTRING(E1_BAIXA,7,2)+'/'+SUBSTRING(E1_BAIXA,5,2)+'/'+SUBSTRING(E1_BAIXA,1,4) AS BAIXA, 
E3_BASE, 
E3_COMIS 
FROM SE3010, SA1010 SA1, SE1010 SE1
WHERE E3_VEND='000010' AND E3_FILIAL='BA' AND A1_COD=E3_CODCLI AND E3_LOJA=A1_LOJA and SE3010.D_E_L_E_T_='' AND E1_NUM=E3_NUM
AND E1_PARCELA=E3_PARCELA
AND E1_BAIXA <> ''
ORDER BY
SUBSTRING(E1_BAIXA,1,4) DESC, 
SUBSTRING(E1_BAIXA,5,2) DESC,
SUBSTRING(E1_BAIXA,7,2) DESC


