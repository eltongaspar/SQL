SELECT 	
	CTE.CTE_EMISSAO AS EMISSAO,
	CTE.CTE_DESTIDCID AS IBGE,
	CTE.CTE_DESTCID AS CIDADE,
	CTE.CTE_DESTUF AS ESTADO,
	C6.C6_ACONDIC,
	C6.C6_METRAGE,
	SUM(C6.C6_LANCES) AS LANCES,
	RTRIM(LTRIM(SUBSTRING(B1.B1_COD,1,10))) 
	+ C6.C6_ACONDIC + REPLICATE('0',(5- LEN(C6.C6_METRAGE))) + CAST(C6.C6_METRAGE AS VARCHAR(5)) AS COD,
	B1.B1_DESC,
	SUM(D2.D2_QUANT) AS QUANTIDADE,
	SUM(D2.D2_QUANT * B1.B1_PESCOB) AS KG,
	SUM(D2.D2_QUANT * D2_PRCVEN) AS TOTAL 

FROM BI_VW_CTES_5A CTE

INNER JOIN SF2010 F2 WITH (NOLOCK) 
	ON F2.F2_CHVNFE		= CTE.NFE_CHAVE
	AND F2.D_E_L_E_T_	= ''
INNER JOIN SD2010 D2 WITH (NOLOCK)
	ON F2.F2_FILIAL		= D2.D2_FILIAL
	AND F2.F2_DOC		= D2.D2_DOC
	AND F2.F2_SERIE     = D2.D2_SERIE
	AND F2.F2_CLIENT    = D2.D2_CLIENTE
	AND F2.F2_LOJA		= D2.D2_LOJA
	AND F2.D_E_L_E_T_	= D2.D_E_L_E_T_
INNER JOIN SF4010 F4 WITH (NOLOCK)
    ON ''								= F4.F4_FILIAL
    AND D2.D2_TES						= F4.F4_CODIGO
    AND D2.D_E_L_E_T_					= F4.D_E_L_E_T_
INNER JOIN SB1010 B1 WITH (NOLOCK)
	ON '' = B1.B1_FILIAL
	AND D2.D2_COD = B1.B1_COD
	AND D2.D_E_L_E_T_ = B1.D_E_L_E_T_
INNER JOIN SC6010 C6 WITH (NOLOCK)
	ON	D2.D2_FILIAL					= C6.C6_FILIAL
	AND D2.D2_PEDIDO					= C6.C6_NUM
	AND D2.D2_ITEMPV					= C6.C6_ITEM
	AND D2.D2_COD						= C6.C6_PRODUTO
	AND D2.D_E_L_E_T_					= C6.D_E_L_E_T_

WHERE B1.B1_TIPO	= 'PA'
	AND F4.F4_DUPLIC	=	'S'
	AND F2.F2_TIPO		= 	'N'
	AND NOT CTE.CTE_DESTIDCID  IS NULL
	And CTE.CTE_CHAVE = '35200589823918003089570000004361591290202896'

GROUP BY 
CTE_DESTIDCID,
CTE.CTE_DESTCID,
CTE.CTE_DESTUF,
CTE.CTE_EMISSAO,
RTRIM(LTRIM(SUBSTRING(B1.B1_COD,1,10)))+ C6.C6_ACONDIC + REPLICATE('0',(5- LEN(C6.C6_METRAGE))) 
+ CAST(C6.C6_METRAGE AS VARCHAR(5)),
B1.B1_DESC,
C6.C6_ACONDIC,
C6.C6_METRAGE