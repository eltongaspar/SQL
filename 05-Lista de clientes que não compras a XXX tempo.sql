-- Lista de clientes que não compras a XXX tempo
SELECT
	A1_COD CODIGO, A1_LOJA LOJA, A1_NOME RAZAO_SOCIAL, A1_END ENDERECO, A1_MUN CIDADE, A1_BAIRRO BAIRRO, A1_DDD DDD, A1_TEL TELEFONE,
 A1_CONTATO CONTATO, A1_ULTCOM ULTIMA_COMPRA
FROM 
	SA1010
WHERE
	SA1010.D_E_L_E_T_=''
	AND A1_COD NOT IN (SELECT F2_CLIENT FROM SF2010 WHERE F2_EMISSAO < '20181231')
	--AND A1_EST ='SP'


	Select * From SA1010 
	Where A1_ULTCOM < '20181231'
	Order By A1_ULTCOM